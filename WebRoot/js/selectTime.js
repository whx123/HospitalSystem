function jump1() {
		var y1= document.getElementById("y1").innerHTML;
		var time1 = document.getElementById("time1").innerHTML;
		var date = document.getElementById("date").innerHTML;
		var doctorId = document.getElementById("doctorId").innerHTML;
		if(y1>0){
		window.location.href = 'registerIn.action?doctorId=' + doctorId + '&time='
				+ time1 + '&date=' + date;
		}
		else{
			alert("当前时间段挂号源已用完");
		}
	}
